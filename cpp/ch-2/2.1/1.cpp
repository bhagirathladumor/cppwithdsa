
#include <iostream>
using namespace std;

int main() {
    int i, j;
    cout << "Enter two numbers: ";
    cin >> i >> j;

    cout << "Addition: " << i + j << endl;
    cout << "Subtraction: " << i - j << endl;
    cout << "Multiplication: " << i * j << endl;
    cout << "Division: " << i / j << endl;
    cout << "Modulus: " << i % j << endl;
}